from shapely.ops import nearest_points

# 구분별 최근접 거리 계산 함수
def calc_min_distance(grid_gdf, facility_gdf, col_name):
    if len(facility_gdf) == 0:
        grid_gdf[col_name] = np.nan
        return grid_gdf

    facility_union = facility_gdf.geometry.unary_union
    distances = []
    for center in grid_gdf['center']:
        dist = center.distance(nearest_points(center, facility_union)[1])
        distances.append(dist / 1000)  # km 단위
    grid_gdf[col_name] = distances
    return grid_gdf

# 구분별 계산
