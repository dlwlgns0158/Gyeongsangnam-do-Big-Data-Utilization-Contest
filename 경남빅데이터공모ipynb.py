# 정규화
scaler = MinMaxScaler()
scaled = scaler.fit_transform(df[['전문의수']])

# 시퀀스 생성
def make_sequences(data, window=3):
    X, y = [], []
    for i in range(len(data)-window):
        X.append(data[i:i+window])
        y.append(data[i+window])
    return np.array(X), np.array(y)

X, y = make_sequences(scaled, window=3)
print(f"X shape: {X.shape}, y shape: {y.shape}")

# LSTM 모델
model = Sequential([
    LSTM(64, return_sequences=True, input_shape=(3,1)),
    Dropout(0.2),
    LSTM(32),
    Dropout(0.2),
    Dense(1)
])
model.compile(optimizer='adam', loss='mse')
model.summary()

# 학습
history = model.fit(X, y, epochs=200, batch_size=4, verbose=0)
